package mavengitTest;

public class Calcolatrice {
	
	
	
	public int moltiplicazione(int a, int b){
		return a*b;
	}
	public float divisione(float a, float b){
		return a/b;
	}
	public int addizione(int a, int b){
		return a+b;
	}
	public int sottrazione(int a, int b){
		return a-b;
	}
	
}
